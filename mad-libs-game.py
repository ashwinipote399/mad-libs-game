# Mad Libs Game using Sense Words

color = input("Enter a color: ")
sound = input("Enter a sound: ")
smell = input("Enter a smell: ")
taste = input("Enter a taste: ")
feeling = input("Enter a feeling: ")
place = input("Enter a place: ")

print("\n--- Your Mad Libs Story ---\n")

print("Today I went to the", place + ".")
print("I saw a", color, "object.")
print("I heard a", sound + ".")
print("The place smelled like", smell + ".")
print("I tasted something", taste + ".")
print("It made me feel very", feeling + ".")
